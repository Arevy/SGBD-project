CREATE TABLE client(
    ID_CLIENT NUMBER PRIMARY KEY NOT NULL,
    NUME VARCHAR2(40) NOT NULL,
    PRENUME VARCHAR2(40) NOT NULL,
    TELEFON VARCHAR2(20) NOT NULL
);
 
CREATE TABLE furnizor(
    ID_FURNIZOR NUMBER PRIMARY KEY NOT NULL,
    NUME VARCHAR2(40) NOT NULL,
    ULTIMA_APROVIZIONARE DATE NOT NULL
);
 
CREATE TABLE adresa(
    ID_ADRESA NUMBER PRIMARY KEY NOT NULL,
    ORAS VARCHAR2(40) NOT NULL,
    STRADA VARCHAR2 (40) NOT NULL
);
 
CREATE TABLE magazin(
    ID_MAGAZIN NUMBER PRIMARY KEY NOT NULL,
    NUME VARCHAR2(40) NOT NULL,
    ID_ADRESA NUMBER REFERENCES adresa(ID_ADRESA)
);
 
CREATE TABLE angajat(
    ID_ANGAJAT NUMBER PRIMARY KEY NOT NULL,
    NUME VARCHAR2(40) NOT NULL,
    PRENUME VARCHAR2(40) NOT NULL,
    SALARIU NUMBER NOT NULL,
    ZILE_CONCEDIU NUMBER NOT NULL,
    DATA_ANGAJARE DATE NOT NULL,
    ID_MAGAZIN NUMBER REFERENCES magazin(ID_MAGAZIN) NOT NULL
);
 
CREATE TABLE produs(
    ID_PRODUS NUMBER PRIMARY KEY NOT NULL,
    NUME VARCHAR2(40) NOT NULL,
    PRET FLOAT NOT NULL,
    TIP VARCHAR2(15) NOT NULL,
    ID_FURNIZOR NUMBER REFERENCES furnizor(ID_FURNIZOR) 
);
 
CREATE TABLE magazin_produs(
    STOC NUMBER NOT NULL,
    ID_MAGAZIN NUMBER REFERENCES magazin(ID_MAGAZIN) NOT NULL,
    ID_PRODUS NUMBER REFERENCES produs(ID_PRODUS) NOT NULL,
    PRIMARY KEY (ID_MAGAZIN, ID_PRODUS)
);
 
CREATE TABLE comanda(
    ID_COMANDA NUMBER PRIMARY KEY NOT NULL,
    PRET_TOTAL NUMBER NOT NULL,
    DATA_COMANDA DATE NOT NULL,
    ID_CLIENT NUMBER  REFERENCES client(ID_CLIENT) NOT NULL
);
 
CREATE TABLE comanda_produs(
    CANTITATE NUMBER NOT NULL,
    ID_COMANDA NUMBER REFERENCES comanda(ID_COMANDA) NOT NULL,
    ID_PRODUS NUMBER REFERENCES produs(ID_PRODUS) NOT NULL,
    PRIMARY KEY (ID_COMANDA, ID_PRODUS)
);



